<template>
	<!-- 로그인 X -->	
	<div v-if="!isLoggedIn" class="title-width">
		<v-card-subtitle>
			<h2>구독</h2>
		</v-card-subtitle>
		<v-list-item-content>
			<h4 class="main-side-menu-contents">
				챌린지를 구독해보세요!
			</h4>
		</v-list-item-content>
	</div>
	<!-- 로그인 O -->
    <v-list v-else class="title-width">
        <v-list-group>
          <template v-slot:activator class="title-background">
            <v-card-subtitle class="title-width">
				<h2 v-if="!isMobile()">구독</h2>
				<h4 v-else>구독</h4>
			</v-card-subtitle>
          </template>
          <v-chip
			v-for="challenge in userInfo.subscriptions"
			:key="challenge.challenge_no"
			:value="challenge"
			:to="{ path: '/challenge/' + challenge.id }"
			v-model="challenge.isOpen"
			color="primary"
			outlined
			close
			@click:close="remove(challenge.challenge_no)">
			{{ challenge.challenge_title }}
			</v-chip>
        </v-list-group>
    </v-list>
</template>

<script>
import { mapState, mapActions } from "vuex";
	
const userStore = "userStore";
export default {
  name: 'MySubscription',
  props: { search: String },
  computed: {
		...mapState(userStore, ["userInfo"]),
		isLoggedIn() {
			return this.$store.state.userStore.isLoggedIn;
		}
	},
  data() {
    return {
    }
  },
  methods: {
	  	...mapActions(userStore, ["getUserInfo"]),
		remove(no) {
			this.$store.dispatch('userStore/deleteSubscription', { no, token: localStorage.getItem('Authorization') })
			setTimeout(() => {
				this.getUserInfo(localStorage.getItem("Authorization"));
			}, 300);
		},
		isMobile() {
            if (
                /Android|webOS|iPhone|iPad|iPod|BlackBerry|IEMobile|Opera Mini/i.test(
                    navigator.userAgent
                )
            ) {
                return true;
            } else {
                return false;
            }
        },
	},
}
</script>

